import React from "react";
import Link from "next/link";

interface DashboardCardProps {
  title: string;
  value: React.ReactNode;
  icon: React.ReactNode;
  subtext?: string;
  href?: string;
  valueClassName?: string;
}

export function DashboardCard({
  title,
  value,
  icon,
  subtext,
  href,
  valueClassName = "text-2xl font-bold text-slate-900",
}: DashboardCardProps) {
  const inner = (
    <div className={`stat-card h-full flex flex-col gap-2 transition-all ${href ? "cursor-pointer hover:shadow-lg" : ""}`}>
      <div className="flex items-start justify-between gap-3">
        <div className="flex-1 min-w-0">
          <p className="dash-month-label mb-2">{title}</p>
          <p className={valueClassName}>{value}</p>
          {subtext && <p className="text-xs text-slate-400 mt-1">{subtext}</p>}
        </div>
        <div className="dashboard-card-icon">{icon}</div>
      </div>
      {href && <p className="dash-card-link">View →</p>}
    </div>
  );

  return href ? (
    <Link href={href} className="block h-full">{inner}</Link>
  ) : (
    inner
  );
}

